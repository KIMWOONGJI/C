#include <stdio.h>

int main(int argc, char const *argv[]) {
  printf("/%12d/  /%12d/  /%12d/\n", 100, 200, 300);
  printf("/%+12d/  /%+12d/  /%+12d/\n", 100, 200, 300);
  printf("/%-12d/  /%-12d/  /%-12d/\n", 100, 200, 300);

  printf("/%12g/  /%12g/  /%12g/\n", 0.00001, 0.000001, 0.0000001);
  printf("/%+12g/  /%+12g/  /%+12g/\n", 0.00001, 0.000001, 0.0000001);
  printf("/%-12g/  /%-12g/  /%-12g/\n", 0.00001, 0.000001, 0.0000001);
  return 0;
}
